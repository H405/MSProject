//==============================================================================
// Brief  : 更新処理
// Return : void								: なし
// Arg    : Point* pPoint						: 対象
//==============================================================================
void PointStateArrange::Update( Point* pPoint )
{
	// 消滅
	if( pPoint->GetTime() >= pPoint->GetTimeExist() )
	{
		pPoint->SetIsEnable( false );
	}
}
