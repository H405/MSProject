//=============================================================================
//	初期化処理
//=============================================================================
void CMovie::Init()
{
	HRESULT result;

	//	COM初期化
	result = CoInitialize(NULL);
	if (result == E_FAIL)
	{
		ASSERT("COM初期化失敗");
	}

	//	フィルタグラフマネージャーの初期化
	result = CoCreateInstance(
		CLSID_FilterGraph,
		NULL,
		CLSCTX_INPROC_SERVER,
		IID_IGraphBuilder,
		(void**)(&graphBuilder));
	if (result == E_FAIL)
	{
		ASSERT("IGraphBuilderインターフェースの取得失敗");
	}

	/*result = graphBuilder->QueryInterface(
		IID_IMediaControl,
		(void**)())*/
}
