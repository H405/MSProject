#include "main.h"
#include "CInput.h"

//*****************************************************************************
//	静的変数定義
//*****************************************************************************
LPDIRECTINPUT8 CInput::input = NULL;

//=============================================================================
//	コンストラクタ
//=============================================================================
CInput::CInput()
{
	inputDevice = NULL;
}
//=============================================================================
//	デストラクタ
//=============================================================================
CInput::~CInput()
{
}
//=============================================================================
//	初期化
//=============================================================================
bool CInput::Init(HINSTANCE _instanceHandle, HWND _windowHandle)
{
	HRESULT hr = S_OK;

	if(input == NULL)
	{
		//	DirectInputオブジェクトの作成
		hr = DirectInput8Create(_instanceHandle,
								DIRECTINPUT_VERSION,
								IID_IDirectInput8,
								(void**)&input,
								NULL);

		if (hr == E_FAIL)
		{
			ASSERT("インプットデバイスの生成失敗");
			return false;
		}
	}

	return true;
}
//=============================================================================
//	更新
//=============================================================================
void CInput::Update()
{
}
//=============================================================================
//	終了
//=============================================================================
void CInput::Uninit()
{
	SAFE_RELEASE(input);

	SAFE_RELEASE(inputDevice);
}

