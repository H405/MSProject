#ifndef MY_SOUND_H
#define MY_SOUND_H

//******************************************************************************
// インクルード
//******************************************************************************
#include "xaudio2.h"

//******************************************************************************
// ライブラリ
//******************************************************************************

//******************************************************************************
// マクロ
//******************************************************************************

//******************************************************************************
// クラス前方宣言
//******************************************************************************

//******************************************************************************
// クラス定義
//******************************************************************************
class Sound
{
public:
	//==============================================================================
	// Brief  : コンストラクタ
	// Return : 									: 
	// Arg    : void								: なし
	//==============================================================================
	Sound( void );

	//==============================================================================
	// Brief  : デストラクタ
	// Return : 									: 
	// Arg    : void								: なし
	//==============================================================================
	~Sound( void );

	//==============================================================================
	// Brief  : 初期化処理
	// Return : int									: 実行結果
	// Arg    : IXAudio2SourceVoice* pSourceVoice	: ソースボイス
	// Arg    : BYTE* pData							: データ
	// Arg    : DWORD size							: データサイズ
	//==============================================================================
	int Initialize( IXAudio2SourceVoice* pSourceVoice, BYTE* pData, DWORD size );

	//==============================================================================
	// Brief  : 終了処理
	// Return : int									: 実行結果
	// Arg    : void								: なし
	//==============================================================================
	int Finalize( void );

	//==============================================================================
	// Brief  : 再初期化処理
	// Return : int									: 実行結果
	// Arg    : IXAudio2SourceVoice* pSourceVoice	: ソースボイス
	// Arg    : BYTE* pData							: データ
	// Arg    : DWORD size							: データサイズ
	//==============================================================================
	int Reinitialize( IXAudio2SourceVoice* pSourceVoice, BYTE* pData, DWORD size );

	//==============================================================================
	// Brief  : クラスのコピー
	// Return : int									: 実行結果
	// Arg    : Sound* pOut							: コピー先アドレス
	//==============================================================================
	int Copy( Sound* pOut ) const;

	//==============================================================================
	// Brief  : 再生
	// Return : void								: なし
	// Arg    : int countLoop						: ループ回数
	//==============================================================================
	void Play( int countLoop = 0 );

	//==============================================================================
	// Brief  : 一時停止
	// Return : void								: なし
	// Arg    : void								: なし
	//==============================================================================
	void Pause( void );

	//==============================================================================
	// Brief  : 一時停止解除
	// Return : void								: なし
	// Arg    : void								: なし
	//==============================================================================
	void Unpause( void );

	//==============================================================================
	// Brief  : 停止
	// Return : void								: なし
	// Arg    : void								: なし
	//==============================================================================
	void Stop( void );

	//==============================================================================
	// アクセサ
	//==============================================================================
	void SetVolume( float value );
	float GetVolume( void ) const;

protected:

private:
	void InitializeSelf( void );
	Sound( const Sound& );
	Sound operator=( const Sound& );

	IXAudio2SourceVoice*	pSourceVoice_;		// ソースボイス
	BYTE*					pData_;				// データ
	DWORD					size_;				// データサイズ
	XAUDIO2_BUFFER			audioBuffer_;		// オーディオバッファ情報
};

#endif	// MY_SOUND_H
