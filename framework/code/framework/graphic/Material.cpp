Material::~Material( void )
{
}

//==============================================================================
// Brief  : クラス内の初期化処理
// Return : void								: なし
// Arg    : void								: なし
//==============================================================================
void Material::InitializeSelf( void )
{
	// メンバ変数の初期化
	diffuse_ = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
	specular_ = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
	ambient_ = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
	emmisive_ = D3DXCOLOR( 1.0f, 1.0f, 1.0f, 1.0f );
	reflection_ = 0.0f;
	power_ = 0.0f;
	refractive_ = 0.0f;
}
